
# Print 1 to 100 in sequence
print("----------Print 1 to 100 in sequence:")
for i in range(0,100):
    print(i)

print("----------Print 10 to 31 in sequence:")
for p in range(10,32,1):
    print(p)

print("----------Print 1 to 10 Even number")
for L in range(1,11,2):
        print(L)
print("----------Print number in Reverse 20 to 10--------")
for n in range(20,9,-1):
    print(n)

